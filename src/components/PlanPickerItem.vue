<template>
  <div @click="selectPlan" 
  class="plan"
  :class="{selected: isSelected}"
  >
    <div class="description">
      <span class="title">
        {{ name }} {{ isSelected ? 'ðŸŒŸ' : '' }}
      </span>
    </div>
  </div>
</template>

<script setup>
//Importamos la funcion para
//crear una propiedad computada

import {computed} from 'vue';

//Usando un macro para
//definir las props
//defineProps(['name', 'planIcon']);
const props = defineProps({
  name: {
    type: String,
    required: true
  },
  selectPlan: String
});
//Define event
const emit = defineEmits(['select']);

//Funcion que permite cambiar el estado de la variable
//selected
const selectPlan = () => {

//Emitir el evento de que el 
//plan ha sido seleccionado
emit('select', props.name);
};

//Creando una propiedad computada
//para saber si el plan ha seleccionado
const isSelected=computed(()=>{
  return props.name === props.selectPlan;
});

</script>

